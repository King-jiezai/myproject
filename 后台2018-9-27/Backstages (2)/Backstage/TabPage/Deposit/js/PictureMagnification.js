
//法人代表或股东身份证正面照片，鼠标移上去图片变大
function FaceImage() {

	var img = $('.FaceImage img')
	var wValue = 3 * img.width();
	var hValue = 3 * img.height();
	img.stop().animate({
		width: wValue,
		height: hValue,
		position: 'relative',
		zIndex: '9999',
		left: ("-" + (2 * img.width()) / 2),
		top: ("-" + (1.8 * img.height()) / 2),

	}, 800);

}

